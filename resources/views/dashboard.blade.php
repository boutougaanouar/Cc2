<script src="https://cdn.jsdelivr.net/npm/chart.js"></script>

<script>
    // Données pour le graphique des parfums par laboratoire
    const parfumsParLaboData = @json($parfumsParLabo);
    
    new Chart(document.getElementById('parfumsParLabo'), {
        type: 'bar',
        data: {
            labels: parfumsParLaboData.map(item => item.laboratoire),
            datasets: [{
                label: 'Nombre de Parfums',
                data: parfumsParLaboData.map(item => item.count),
                backgroundColor: 'rgba(99, 102, 241, 0.8)',
                borderColor: 'rgb(99, 102, 241)',
                borderWidth: 1
            }]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            scales: {
                y: {
                    beginAtZero: true,
                    ticks: {
                        stepSize: 1
                    }
                }
            }
        }
    });

    // Données pour le graphique des produits les plus utilisés
    const produitsUtilisesData = @json($produitsUtilises);
    
    new Chart(document.getElementById('produitsUtilises'), {
        type: 'pie',
        data: {
            labels: produitsUtilisesData.map(item => item.produit),
            datasets: [{
                data: produitsUtilisesData.map(item => item.utilisations),
                backgroundColor: [
                    'rgba(99, 102, 241, 0.8)',
                    'rgba(16, 185, 129, 0.8)',
                    'rgba(245, 158, 11, 0.8)',
                    'rgba(239, 68, 68, 0.8)',
                    'rgba(139, 92, 246, 0.8)'
                ]
            }]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            plugins: {
                legend: {
                    position: 'bottom'
                }
            }
        }
    });
</script>
